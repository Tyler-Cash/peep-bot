const minutesAgo = (n: number) => new Date(Date.now() - n * 60_000).toISOString();
const minutesFromNow = (n: number) => new Date(Date.now() + n * 60_000).toISOString();

const histOk = (
  stage: string,
  type: string,
  listener: string,
  ts: string,
): AdminEventHistoryEntry => ({
  stage,
  lifecycleEventType: type,
  listenerName: listener,
  ts,
  ok: true,
  attempts: 1,
  detail: null,
});

const histFail = (
  stage: string,
  type: string,
  listener: string,
  ts: string,
  detail: string,
  attempts = 3,
): AdminEventHistoryEntry => ({
  stage,
  lifecycleEventType: type,
  listenerName: listener,
  ts,
  ok: false,
  attempts,
  detail,
});

